    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        AddEditForm frmAddEdit = new AddEditForm(this, true);
        frmAddEdit.setVisible(true);
        String[] fields = frmAddEdit.getFieldValues();
        if (!frmAddEdit.wasCancelled()) {
            app.addContact(fields[0], fields[1], fields[2], fields[3]);
            jList1.setListData(app.getContactsList());
        }


    }//GEN-LAST:event_btnAddActionPerformed

