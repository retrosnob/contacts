    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        // This runs when I click a name in the JList. 
        // Make the fields change to the select contact's details.

        int index = jList1.getSelectedIndex();
        if (index > -1) {
            String[] fields = app.getContactFields(index);
            this.txtFirstName.setText(fields[0]);
            this.txtLastName.setText(fields[1]);
            this.txtPhone.setText(fields[2]);
            this.txtEmail.setText(fields[3]);
        }


    }//GEN-LAST:event_jList1ValueChanged

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = jList1.getSelectedIndex();
        if (index > -1) {
            app.deleteContact(index);
            jList1.setListData(app.getContactsList());
        } else {
            lblMsg.setText("No contact selected");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:

        AddEditForm frmAddEdit = new AddEditForm(this, true);

        int index = jList1.getSelectedIndex();
        if (index > -1) {
            String[] fields = app.getContactFields(index);
            frmAddEdit.setFieldValues(fields);
            frmAddEdit.setVisible(true);
            if (!frmAddEdit.wasCancelled()) {
                fields = frmAddEdit.getFieldValues();
                app.editContact(index, fields[0], fields[1], fields[2], fields[3]);
                jList1.setListData(app.getContactsList());
            }
        } else {
            lblMsg.setText("No contact selected");
        }


    }//GEN-LAST:event_btnEditActionPerformed

